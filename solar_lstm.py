import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import joblib

from sklearn.preprocessing import MinMaxScaler
from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import LSTM, Dense, Dropout, Input

# =================================
# 1 Load Dataset
# =================================

power = pd.read_csv("/content/Plant_1_Generation_Data.csv") # Corrected file path
weather = pd.read_csv("/content/Plant_1_Weather_Sensor_Data.csv")

power['DATE_TIME'] = pd.to_datetime(power['DATE_TIME'], format='%d-%m-%Y %H:%M')
weather['DATE_TIME'] = pd.to_datetime(weather['DATE_TIME'], format='%Y-%m-%d %H:%M:%S') # Specified format for consistency

data = pd.merge(power, weather, on="DATE_TIME")

data = data[[
    "AC_POWER",
    "IRRADIATION",
    "AMBIENT_TEMPERATURE",
    "MODULE_TEMPERATURE"
]]

data = data.dropna()

# =================================
# 2 Separate Features and Target
# =================================

X = data[[
    "IRRADIATION",
    "AMBIENT_TEMPERATURE",
    "MODULE_TEMPERATURE"
]]

y = data["AC_POWER"]

# =================================
# 3 Normalize
# =================================

scaler_X = MinMaxScaler()
scaler_y = MinMaxScaler()

X_scaled = scaler_X.fit_transform(X)
y_scaled = scaler_y.fit_transform(y.values.reshape(-1,1))

# Save scalers
joblib.dump(scaler_X,"scaler_X.pkl")
joblib.dump(scaler_y,"scaler_y.pkl")

# =================================
# 4 Create Sequences
# =================================

SEQ_LENGTH = 24

X_seq = []
y_seq = []

for i in range(len(X_scaled) - SEQ_LENGTH):

    X_seq.append(X_scaled[i:i+SEQ_LENGTH])
    y_seq.append(y_scaled[i+SEQ_LENGTH])

X_seq = np.array(X_seq)
y_seq = np.array(y_seq)

print("Shape:",X_seq.shape)

# =================================
# 5 Train Test Split
# =================================

split = int(len(X_seq)*0.8)

X_train = X_seq[:split]
X_test = X_seq[split:]

y_train = y_seq[:split]
y_test = y_seq[split:]

# =================================
# 6 Build LSTM Model
# =================================

model = Sequential([

    Input(shape=(SEQ_LENGTH,3)),

    LSTM(64,return_sequences=True),
    Dropout(0.2),

    LSTM(32),

    Dense(16),
    Dense(1)

])

model.compile(
    optimizer="adam",
    loss="mse"
)

model.summary()

# =================================
# 7 Train
# =================================

history = model.fit(

    X_train,
    y_train,

    epochs=25,
    batch_size=32,

    validation_data=(X_test,y_test)
)

# =================================
# 8 Save Model
# =================================

model.save("solar_lstm_model2.keras")

print("Model Saved")

# =================================
# 9 Predict
# =================================

pred = model.predict(X_test)

# Convert back to original scale
pred_real = scaler_y.inverse_transform(pred)
y_test_real = scaler_y.inverse_transform(y_test)

# =================================
# 10 Plot
# =================================

plt.figure(figsize=(12,5))

plt.plot(y_test_real[:200],label="Actual")
plt.plot(pred_real[:200],label="Predicted")

