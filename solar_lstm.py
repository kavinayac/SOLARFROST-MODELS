import pandas as pd
import numpy as np
import matplotlib.pyplot as plt

from sklearn.preprocessing import MinMaxScaler
from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import LSTM, Dense, Input
from tensorflow.keras.models import load_model

# ===============================
# 1 Load Dataset
# ===============================

power = pd.read_csv("/Plant_1_Generation_Data.csv")
weather = pd.read_csv("/Plant_1_Weather_Sensor_Data.csv")

# Convert 'DATE_TIME' columns to datetime objects for consistent merging
power['DATE_TIME'] = pd.to_datetime(power['DATE_TIME'], format='%d-%m-%Y %H:%M')
weather['DATE_TIME'] = pd.to_datetime(weather['DATE_TIME'], format='%Y-%m-%d %H:%M:%S')

# Merge datasets
data = pd.merge(power, weather, on="DATE_TIME")

# Select important features
data = data[
    [
        "AC_POWER",
        "IRRADIATION",
        "AMBIENT_TEMPERATURE",
        "MODULE_TEMPERATURE"
    ]
]

# Remove missing values
data = data.dropna()

# ===============================
# 2 Normalize Data
# ===============================

scaler = MinMaxScaler()
scaled_data = scaler.fit_transform(data)

# ===============================
# 3 Create Time Sequences
# ===============================

def create_sequences(data, seq_length=24):

    X = []
    y = []

    for i in range(len(data) - seq_length):

        X.append(data[i:i+seq_length])
        y.append(data[i+seq_length][0])

    return np.array(X), np.array(y)

X, y = create_sequences(scaled_data)

# ===============================
# 4 Train Test Split
# ===============================

split = int(len(X) * 0.8)

X_train = X[:split]
X_test = X[split:]

y_train = y[:split]
y_test = y[split:]

# ===============================
# 5 Build LSTM Model
# ===============================

model = Sequential([

    Input(shape=(X.shape[1], X.shape[2])),

    LSTM(64, return_sequences=True),

    LSTM(32),

    Dense(1)

])

model.compile(
    optimizer="adam",
    loss="mse"
)

model.summary()

# ===============================
# 6 Train Model
# ===============================

history = model.fit(

    X_train,
    y_train,

    epochs=20,
    batch_size=32,

    validation_data=(X_test, y_test)

)

# ===============================
# 7 Save Model
# ===============================

model.save("solar_forecast_lstm.keras")

print("Model saved successfully")

# ===============================
# 8 Prediction
# ===============================

pred = model.predict(X_test)

# ===============================
# 9 Plot Results
# ===============================

plt.figure(figsize=(10,5))

plt.plot(y_test[:200], label="Actual")
